package main

import "fmt"

func main() {
	var isMarried bool = true // deklarasi variabel dengan tipe data boolean
	var myAge int = 20 // deklarasi variabel dengan tipe data integer
	var pi float32 = 3.14 // deklarasi variabel dengan tipe data float32
	var myName string = "Rifqy Saputra" // deklarasi variabel dengan tipe data string

	fmt.Println("Boolean :", isMarried) // mencetak nilai variabel isMarried
	fmt.Println("Integer :", myAge) // mencetak nilai variabel myAge
	fmt.Println("Float32 :", pi) // mencetak nilai variabel pi
	fmt.Println("String :", myName) // mencetak nilai variabel myName
}